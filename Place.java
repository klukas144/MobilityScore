package de.hka.projekt.objects;

public class Place {

    private int uid;

    private double lat;

    private double lng;

    private boolean bike;

    private boolean spot;

    public int getUid() {
        return uid;
    }

    public double getLat() {
        return lat;
    }

    public double getLng() {
        return lng;
    }

    public boolean isBike() {
        return bike;
    }

    public boolean isSpot() {
        return spot;
    }
}
